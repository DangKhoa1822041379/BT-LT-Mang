public class Vidu {
    public static void main(String[] args) {
        for(int i = 1; i <= 100; i++) {
            System.out.println("Bài tập thứ " + i + " trong ngày.");
        }
        for(int i = 100; i > 0; i--) {
            System.out.println("Số lần làm bài tập: " + i);
        }
    }
}
